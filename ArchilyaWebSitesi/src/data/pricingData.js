export function calcPrice(basePrice, m2) {
  return Math.round(basePrice * Math.pow(m2 / 100, 0.8));
}

export function unitSavingPct(m2) {
  if (m2 <= 100) return 0;
  return Math.round((1 - Math.pow(m2 / 100, -0.2)) * 100);
}
